function syncDelay(milliseconds) {
    var start = new Date().getTime();
    var end = 0;
    while ((end - start) < milliseconds) {
        end = new Date().getTime();
    }
}

function resize(evt) {
    evt.preventDefault();
    move += -0.001 * evt.deltaY;
    move = Math.min(10, move);
    move = Math.max(0, move);
    Update();
}

